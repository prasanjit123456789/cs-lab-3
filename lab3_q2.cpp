//Operation of integers
#include<iostream>
using namespace std;

int main()
{
  int a=7;
  int b=9;
  int c=a+b;
  int d=a-b;
  int e=a*b;
  int f=b/a;
  int g=b%a;
  cout <<"7+9="<< c<<"\n";
  cout <<"7-9="<< d<<"\n";
  cout <<"7*9="<< e<<"\n";
  cout <<"The cosent of 9/7="<< f<<"\n";
  cout <<"The reminder of 9/7="<< g<<"\n";
  return 0;
}
